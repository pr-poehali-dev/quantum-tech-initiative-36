function calcArcana(day: number, month: number, year: number): number {
  const digits = `${day}${month}${year}`.split("").map(Number);
  let sum = digits.reduce((a, b) => a + b, 0);
  while (sum > 22) {
    sum = String(sum).split("").map(Number).reduce((a, b) => a + b, 0);
  }
  return sum === 0 ? 22 : sum;
}

